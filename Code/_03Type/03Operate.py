"""
파이썬의 연산자
먼저 사칙연산
+ , - , * , /, %

"""
print(10 % 3)
print(10 / 2)
print(type(10 / 2)) #<class 'float'> 으로 리턴함


"""
목과 나머지, 제곱연산
//, %, **
양끝에 2개는 완전 처음봄 
"""
print(10 / 3) #값 : 3.3333333333333335
print(10 // 3) #값 : 3, 자료형은 <class 'int'> 로 리턴

print(2 ** 5) #값 : 32 
# 엥????? -> 저건 2의 5제곱이란 거임 그러니까 print(2 * 2 * 2 * 2 * 2) 랑 같은거임 

